void main() {
  // 수식
  print(5 % 2);   //2.5
  print(5 / 2);   // 나머지 1
  print('5 % 2 = ${5 % 2}');

     // 변수
    // int x; // Integer 정수 변수 선언(declare)
    // x = 10; // 변수에 x에 10을 대입(assign)

  int x;    // 선언과 동시에 대입(초기화)
  x = 10;   // 변수 X의 할당=대입(assign)
  print('저는요, ${x - 2}살인데요, ');
  print('저는요, $x살인데요, ');
}
